package com.basic;

public class SwapNumbers {

	public static void main(String[] args) {
		int num1,num2,num3;
		num1=9;
		num2=7;
		System.out.println("The Num1 Value is "+num1);
		System.out.println("The Num2 value is "+num2);
		num3=num1;
		num1=num2;
		num2=num3;
		System.out.println("Now num1 Value is "+num1);
		System.out.println("Now num2 Value is "+num2);
		
		

	}

}
